function needJacket(temperature) {
    var hasCoat = false;
    if (temperature < -20 && hasCoat === false) {
        return "Don't leave the house, it's freezing!";
    } else if (temperature < 0) {
        return "Brrr! A hat and a jacket would be best today!";
    } else if (temperature < 10) {
        return "Remember your jacket!";
    } else {
        return "Seems nice today!";
    }
}

 










